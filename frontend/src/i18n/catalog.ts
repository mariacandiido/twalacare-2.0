const enPairs = sharedPairs.map(([source, en]) => [source, en] as [string, string]);
const frPairs = sharedPairs.map(([source, , fr]) => [source, fr] as [string, string]);

function byLongestSourceFirst(entries: Array<[string, string]>) {
  return [...entries].sort((a, b) => b[0].length - a[0].length);
}

export const translationPairs: TranslationPairs = {
  en: byLongestSourceFirst(enPairs),
  fr: byLongestSourceFirst(frPairs),
};

const exactTranslationMaps = {
  en: new Map(translationPairs.en.map(([from, to]) => [normalizeLookupKey(from), to])),
  fr: new Map(translationPairs.fr.map(([from, to]) => [normalizeLookupKey(from), to])),
} satisfies Record<Exclude<LanguageCode, "pt">, Map<string, string>>;

export function normalizeLanguage(input: string | null | undefined): LanguageCode {
  if (input === "en" || input === "fr") return input;
  return "pt";
}

export function getLanguageMeta(lang: LanguageCode) {
  if (lang === "en") return { short: "EN", label: "English (EN)" };
  if (lang === "fr") return { short: "FR", label: "Français (FR)" };
  return { short: "PT", label: "Português (PT)" };
}

export function translateContent(source: string, lang: LanguageCode) {
  const normalizedSource = normalizeBrand(source);

  if (lang === "pt") return normalizedSource;

  const protectedTokens = ["TwalaCARE", "suporte@twalacare.ao", "twalacare.ao"];
  let translated = normalizedSource;

  protectedTokens.forEach((token, index) => {
    translated = translated.replaceAll(token, `__TWALA_TOKEN_${index}__`);
  });

  const exactTranslation = exactTranslationMaps[lang].get(normalizeLookupKey(normalizedSource));

  if (exactTranslation) {
    translated = exactTranslation;
  } else {
    translationPairs[lang].forEach(([from, to]) => {
      translated = translated.replaceAll(normalizeBrand(from), to);
    });
  }

  protectedTokens.forEach((token, index) => {
    translated = translated.replaceAll(`__TWALA_TOKEN_${index}__`, token);
  });

  return translated;
}
